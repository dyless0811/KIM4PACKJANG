package semi.beans;

public class WishlistVo {
	private String productFileSavename;
	private int productNo;
	private String productName;
	private int price;
	private int wishlistNo;
	public WishlistVo() {
		super();
	}
	public String getProductFileSavename() {
		return productFileSavename;
	}
	public void setProductFileSavename(String productFileSavename) {
		this.productFileSavename = productFileSavename;
	}
	public int getProductNo() {
		return productNo;
	}
	public void setProductNo(int productNo) {
		this.productNo = productNo;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getWishlistNo() {
		return wishlistNo;
	}
	public void setWishlistNo(int wishlistNo) {
		this.wishlistNo = wishlistNo;
	}
	
	
}
